let output = 'export type Locale = "en" | "id";\n\n';
output += "export type TranslationKey =\n" + typeLines + ";\n\n";
output += "function buildDict(obj: Record<string, string>): Record<TranslationKey, string> {\n";
output += "  return obj as Record<TranslationKey, string>;\n";
output += "}\n\n";
output += "const enDict: Record<TranslationKey, string> = buildDict(" + JSON.stringify(en, null, 2) + ");\n\n";
output += "const idDict: Record<TranslationKey, string> = buildDict(" + JSON.stringify(id, null, 2) + ");\n\n";
output += 'const translations: Record<Locale, Record<TranslationKey, string>> = { en: enDict, id: idDict };\n\n';
output += 'export function t(locale: Locale, key: TranslationKey, params?: Record<string, string | number>): string {\n';
output += "  const value = translations[locale]?.[key] ?? translations.en[key] ?? key;\n";
output += "  if (!params) return value;\n";
output += "  return Object.entries(params).reduce<string>(\n";
output += "    (result, [param, replacement]) => result.replace(new RegExp('\\\\\\\\{\\\\\\\\{' + param + '\\\\\\\\}\\\\\\\\}', 'g'), String(replacement)),\n";
output += "    value,\n";
output += "  );\n";
output += "}\n";

fs.writeFileSync("src/lib/i18n.ts", output);
