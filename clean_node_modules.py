#!/usr/bin/env python3

import os
import stat
import shutil
import sys
from pathlib import Path

# ── ANSI colors ────────────────────────────────────────────────────────────────
RED     = "\033[91m"
GREEN   = "\033[92m"
YELLOW  = "\033[93m"
CYAN    = "\033[96m"
BOLD    = "\033[1m"
DIM     = "\033[2m"
RESET   = "\033[0m"

TARGET = "node_modules"

def format_size(size_bytes: int) -> str:
    for unit in ("B", "KB", "MB", "GB"):
        if size_bytes < 1024:
            return f"{size_bytes:.1f} {unit}"
        size_bytes /= 1024
    return f"{size_bytes:.1f} TB"

def get_dir_size(path: Path) -> int:
    total = 0
    try:
        for entry in path.rglob("*"):
            if entry.is_file(follow_symlinks=False):
                try:
                    total += entry.stat().st_size
                except OSError:
                    pass
    except PermissionError:
        pass
    return total

def find_node_modules(root: Path) -> list[Path]:
    """
    Walk the tree but skip descending INTO node_modules —
    only collect the top-level occurrences.
    """
    found = []
    for dirpath, dirnames, _ in os.walk(root):
        current = Path(dirpath)
        if TARGET in dirnames:
            found.append(current / TARGET)
            dirnames.remove(TARGET)   # don't recurse inside it
    return found

# ── Windows "Access Denied" fix ────────────────────────────────────────────────
def _force_remove(func, path, exc_info):
    """
    onerror handler for shutil.rmtree.
    On Windows, files inside node_modules are often marked read-only.
    Strip the read-only bit and retry the failed operation.
    """
    try:
        os.chmod(path, stat.S_IWRITE | stat.S_IREAD)
        func(path)
    except Exception as e:
        print(f"    {RED}⚠  Could not force-remove: {path}{RESET}")
        print(f"       {DIM}{e}{RESET}")

def delete(path: Path) -> tuple[bool, int]:
    size = get_dir_size(path)
    try:
        shutil.rmtree(path, onerror=_force_remove)
        # Confirm it's actually gone
        if path.exists():
            print(f"  {RED}✖  Still exists after removal attempt{RESET}")
            return False, 0
        return True, size
    except Exception as exc:
        print(f"  {RED}✖  Fatal error: {exc}{RESET}")
        return False, 0

def main() -> None:
    root = Path(sys.argv[1]).resolve() if len(sys.argv) > 1 else Path.cwd()

    print(f"\n{BOLD}{CYAN}{'─' * 60}{RESET}")
    print(f"{BOLD}{CYAN}  node_modules Cleaner{RESET}")
    print(f"{BOLD}{CYAN}{'─' * 60}{RESET}")
    print(f"{DIM}  Root  : {root}{RESET}")
    print(f"{DIM}  Target: {TARGET}{RESET}")
    print(f"{BOLD}{CYAN}{'─' * 60}{RESET}\n")

    print(f"{YELLOW}⟳  Scanning ...{RESET}\n")
    targets = find_node_modules(root)

    if not targets:
        print(f"{GREEN}✔  No {TARGET} directories found. Nothing to do.{RESET}\n")
        return

    print(f"{BOLD}Found {len(targets)} director{'y' if len(targets) == 1 else 'ies'}:{RESET}\n")
    for t in targets:
        size = get_dir_size(t)
        print(f"  {DIM}↳{RESET}  {t}  {DIM}({format_size(size)}){RESET}")

    print()
    print(f"{BOLD}{CYAN}{'─' * 60}{RESET}")
    print(f"{BOLD}{RED}  Deleting ...{RESET}")
    print(f"{BOLD}{CYAN}{'─' * 60}{RESET}\n")

    total_freed = 0
    deleted_count = 0
    failed_count = 0

    for idx, path in enumerate(targets, 1):
        rel = path.relative_to(root) if path.is_relative_to(root) else path
        print(f"  [{idx}/{len(targets)}] {YELLOW}{rel}{RESET}")
        ok, freed = delete(path)
        if ok:
            print(f"         {GREEN}✔  Deleted  {DIM}({format_size(freed)} freed){RESET}")
            total_freed += freed
            deleted_count += 1
        else:
            print(f"         {RED}✖  Failed   {DIM}(close any editors/terminals using those folders){RESET}")
            failed_count += 1
        print()

    print(f"{BOLD}{CYAN}{'─' * 60}{RESET}")
    print(f"{BOLD}  Summary{RESET}")
    print(f"{BOLD}{CYAN}{'─' * 60}{RESET}")
    print(f"  {GREEN}✔  Deleted : {deleted_count}{RESET}")
    if failed_count:
        print(f"  {RED}✖  Failed  : {failed_count}  {DIM}(close any editors/terminals using those folders){RESET}")
    print(f"  {CYAN}💾  Freed   : {format_size(total_freed)}{RESET}")
    print(f"{BOLD}{CYAN}{'─' * 60}{RESET}\n")

if __name__ == "__main__":
    main()