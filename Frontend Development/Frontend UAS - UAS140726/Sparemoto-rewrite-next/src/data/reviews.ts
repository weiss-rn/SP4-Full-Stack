export function getReviewsByProductId(productId: string): Review[] {
  return reviews.filter((r) => r.productId === productId);
}

export function getReviewStats(productId: string) {
  const productReviews = getReviewsByProductId(productId);
  if (productReviews.length === 0) return { total: 0, average: 0, distribution: [0, 0, 0, 0, 0] };
  const total = productReviews.length;
  const sum = productReviews.reduce((acc, r) => acc + r.rating, 0);
  const distribution = [0, 0, 0, 0, 0];
  for (const r of productReviews) {
    const idx = Math.max(0, Math.min(4, Math.round(r.rating) - 1));
    distribution[idx]++;
  }
  return { total, average: Math.round((sum / total) * 10) / 10, distribution };
}
